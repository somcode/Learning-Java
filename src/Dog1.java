
public class Dog1 {
	
	private int legs;
	private String hair;
	private boolean tail;
	
	//constructor
	public Dog1() {
		
	}
	
	//getter and setter
	public int getLegs() {
		return legs;
	}
	
	public void setLegs(int legs) {
		this.legs = legs;
	}

	public String getHair() {
		return hair;
	}
	
	public void setHair(String hair) {
		this.hair = hair;
	}
	
	public boolean getTail() {
		return tail;
		
	}
	
	public void setTail(boolean tail) {
		this.tail = tail;
	}
	
}
