package Casting;

public class Csat2 {
	public static void main(String[] args) {
	
	double s = 50.22;
	long l = (long)s; //explicit type casting required
	int i = (int)1;   //explicit type casting required
	
	System.out.println("Double value: " + s);
	System.out.println("Long value: " + l);
	System.out.println("Int value: " + i);
	
	
	}

}
